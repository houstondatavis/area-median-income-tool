library(shiny)
library(leaflet)
library(rgdal)

# Define UI for application that draws a histogram
ui <- fluidPage(
  
   # Application title
   titlePanel("Area Median Income Calculator"),
   
   # Sidebar with a slider input for number of bins 
   sidebarLayout(
      sidebarPanel(
        textOutput("pop_numbers"),
        plotOutput("pop_plot")
      ),
      
      # Show a plot of the generated distribution
      mainPanel(
         leafletOutput("mymap")
      )
   )
)

# Define server logic required to draw a histogram
server <- function(input, output) {
   
  blockgroups <- rgdal::readOGR('data/BlockGroupsHC/BlockGroupsHC.shp') %>%
    spTransform(CRSobj = CRS("+init=epsg:4326"))
  
  ami_data <- read.csv('data/income_ami.csv')
  
  blockgroups_with_ami_data <- merge(
    blockgroups,
    ami_data,
    by = "GEOID"
  )
  
  #bg_js <- geojsonio::geojson_json(blockgroups_with_ami_data)
  selectedBG <- reactive({
    
    click <- input$mymap_shape_click
    
    if(is.null(click))
      return()   
    
    #pulls lat and lon from shiny click event
    lat <- click$lat
    lon <- click$lng
    
    #puts lat and lon for click point into its own data frame
    coords <- as.data.frame(cbind(lon, lat))
    
    #converts click point coordinate data frame into SP object, sets CRS
    point <- SpatialPoints(coords)
    proj4string(point) <- CRS("+init=epsg:4326")
    print("Point:")
    print(point)
    
    
    #retrieves country in which the click point resides, set CRS for country
    selected <- blockgroups_with_ami_data[point,]
    print("Selected:")
    print(selected@data[38:42])
    selected@data
    
  })
 
  observe({
    output$pop_plot <- renderPlot({
      barplot(as.numeric(selectedBG()[38:42]))
    })
  })
  observe({
    output$pop_numbers <- renderText({
      selectedBG()[["NAMELSAD"]]
    })
  })
    
  
  output$mymap <- renderLeaflet({
    leaflet(blockgroups_with_ami_data) %>%
    addTiles() %>%
    addPolygons(
                color = "#CCCCCC", 
                fillColor = ~colorQuantile("YlOrRd", pct_below_eli)(pct_below_eli),
                opacity = 1.0,
                fillOpacity = 0.5,
                weight = 1,
                smoothFactor = 0.5,
                highlightOptions = highlightOptions(
                  color = "green",
                  fillColor = "green",
                  weight = 2,
                  bringToFront = TRUE
                )
              )
   })
  

}

# Run the application 
shinyApp(ui = ui, server = server)

